package jac.infosyst.proyectogas.modelo;

public class UsuarioInfo {
        private int id;
        private String nombre;

        public UsuarioInfo(int id, String nombre) {
            this.id = id;
            this.nombre = nombre;


        }

        public int getId() {
            return id;
        }

        public String getnombre() {
            return nombre;
        }



}
