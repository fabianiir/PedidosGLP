package jac.infosyst.proyectogas.modelo;

public class Usuario {

    private String Oid;
    private String nombre;
    private String UserName;
    private String rol;

    public String getId() {
        return Oid;
    }

    public String getnombre() {
        return nombre;
    }
    public String getUserName() {
        return UserName;
    }

    public String getrol() {
        return rol;
    }
}
